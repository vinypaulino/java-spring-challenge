package com.vinypaulino.challenge.ubs.domain.model;

public class Scores {

	private int size;
	private int adaptation_for_seniors;
	private int medical_equipment;
	private int medicine;

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getAdaptation_for_seniors() {
		return adaptation_for_seniors;
	}

	public void setAdaptation_for_seniors(int adaptation_for_seniors) {
		this.adaptation_for_seniors = adaptation_for_seniors;
	}

	public int getMedical_equipment() {
		return medical_equipment;
	}

	public void setMedical_equipment(int medical_equipment) {
		this.medical_equipment = medical_equipment;
	}

	public int getMedicine() {
		return medicine;
	}

	public void setMedicine(int medicine) {
		this.medicine = medicine;
	}

}
